function TANGER_OCX_OpenDoc(docid)
{
	TANGER_OCX_OBJ = document.all.item("TANGER_OCX");
	if(docid != "")
	{	
		TANGER_OCX_OBJ.BeginOpenFromURL("readdoc.jsp?docid=" + docid);
	}
	else
	{
		TANGER_OCX_OBJ.CreateNew("Word.Document");
	}
}

